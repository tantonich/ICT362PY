# Sample Program #1 with Simple Comments
#printing a string with white space
print ("Hello World!\n")
#printing the result of long integer operation
print(123123123123123123123123123123123123123123123123 + 1)
#printing a value in decimal, octal, hex, and binary
print(10)
print(0o10)
print(0x10)
print(0b10)
#using the escape character to print symbols in a string
print('This string contains a single quote (\') character.')
print("This string contains a double quote (\") character.")
#continuing a print of string in multiple lines
print('a\
... b\
... c')
#using the escape character and more white space
print('foo\\bar')
print('foo\tbar')
#using the ASCII and Hex values of the "lower case a" character
print("a\141\x61")
#assigning values to variables
a = 4
b = 3
#reassigning a value to the variable a
a=b
#using unary operators
+a
-b
#using basic and not-so-simple math operators
print(a % b)
print(a ** b)